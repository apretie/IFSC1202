#While loop repeats the block of statements,
    # while some condition evaluates to True.
#When the condition is evaluated to False, 
    #the block of statements is executed no more
    #while loop is ended.
#The condition is given before the loop body (before the block of statements) 
    #and is checked before each execution of the loop body.
#the while loop is used when it is impossible or inconvenient to determine the exact number of loop iterations in advance.
#Example of format:

#while some condition:
    #a block of statements

