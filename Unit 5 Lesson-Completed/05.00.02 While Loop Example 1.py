#This program prints squares of all integers from 1 to 10. 
# (Here you can replace the while loop by the for ... in range(...) loop.)

i = 1
while i <= 10:
    print(i ** 2)
    i += 1

#the variable i inside the loop iterates from 1 to 10.
#counter = a variable whose value changes with each new loop iteration
#Note that after executing this fragment the value of the variable i is defined and is equal to 11
    #because when i == 11 the condition i <= 10 is False for the first time.