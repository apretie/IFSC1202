x  = str(input("Enter a string: "))
print(x[2])  #print the third character of this string.
print(x[-2])  #print the second to last character of this string.
print(x[:5]) #print the first five characters of this string.
print(x[:-2]) #print all but the last two characters of this string.
print(x[::2]) #print all the characters of this string with even indices (remember indexing starts at 0, so the characters are displayed starting with the first).
print(x[1::2]) #prints 1 index to end all odd indicies
print(x[::-1]) #print all the characters of the string in reverse order.
print(x[::-2]) #print every second character of the string in reverse order, starting from the last one.
print(len(x))  #print total characters in string


