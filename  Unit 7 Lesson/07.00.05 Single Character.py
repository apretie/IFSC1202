#A slice gives from the given string one character or some fragment: 
    # substring or subsequence.

#The simplest form of slice is a single character slice: 
    # S[i] gives ith character of the string. 
    # Now, the hardest thing to get in all this string business: 
        # indexing starts at 0; the first element has index 0, the second has index 1, and so on.

#Example:  That is, if S='Hello', S[0]=='H', S[1]=='e', S[2]=='l', S[3]=='l', S[4]=='o'.

S = 'Hello'
print(S[0])
print(S[1])
print(S[2])
print(S[3])
print(S[4])